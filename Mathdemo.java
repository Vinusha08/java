package name.java;

public class Mathdemo {
	public static void main(String[] args) {
        int number= 109;
	        float number1 = 109.6f;
	        System.out.println(Math.abs(number1));
	        System.out.println(Math.ceil(number1));
	        System.out.println(Math.floor(number1));
	        System.out.println(Math.max(number, number1));
	        System.out.println(Math.min(number, number1));
	        System.out.println(Math.log(number1));
	        System.out.println(Math.exp(number1));
	    
	}

}
